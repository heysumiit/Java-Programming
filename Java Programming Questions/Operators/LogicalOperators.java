public class LogicalOperators{
    public static void main(String[] args){
       
        boolean a = true;
        boolean b = false;

        System.out.println("A && B : "+(a && b));       // Logical AND
        System.out.println("A || B : "+(a || b));       // Logical OR
        System.out.println("!A : "+(!a));               // Logical NOT

    }
}